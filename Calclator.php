<?php

class Calclator
{
    function plus($a, $b)
    {
        return $a + $b;
    }

    function minus($a, $b)
    {
        return $a - $b;
    }

    function multiply($a, $b)
    {
        return $a * $b;
    }

    function divide($a, $b)
    {
        return $a / $b;
    }

    function square($a)
    {
        return $a * $a;
    }
}
